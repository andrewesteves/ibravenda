    /**
     * Verifica se o produto já está inserido no ArrayList pedido_produto caso
     * esteja atualiza com a nova informação de quantidade
     *
     * @param pp
     * @return boolean
     */
    private boolean encontrarPedidoProduto(PedidoProduto pp) {
        boolean achou = false;
        for (PedidoProduto ppd : pedido_produto) {
            if (ppd.getProduto().getCodigo() == pp.getProduto().getCodigo()) {
                ppd.setQuantidade(Integer.parseInt(jTextFieldProdutoQuantidade.getText()));
                achou = true;
            }
        }
        return achou;
    }

    private void jButtonAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarProdutoActionPerformed
        // TODO add your handling code here:
        if (jTextFieldProdutoQuantidade.getText().trim().equals("") == false) {

            try {
                if (jComboBoxProduto.getSelectedIndex() < 0) {
                    throw new Exception("Informe o produto.");
                }
                Produto prd = new Produto();
                prd.setCodigo(produtos.get(jComboBoxProduto.getSelectedIndex()).getCodigo());

                try {
                    for (int i = 0; i < produtos.size(); i++) {
                        if (produtos.get(i).getCodigo() == prd.getCodigo() && produtos.get(i).getEstoque() < Integer.parseInt(jTextFieldProdutoQuantidade.getText())) {
                            throw new Exception("O estoque do produto é " + produtos.get(i).getEstoque());
                        }
                    }
                    PedidoProduto pp = new PedidoProduto();
                    pp.setProduto(produtos.get(jComboBoxProduto.getSelectedIndex()));
                    pp.setPreco(pp.getProduto().getPreco());
                    if (!encontrarPedidoProduto(pp)) {
                        pp.setQuantidade(Integer.parseInt(jTextFieldProdutoQuantidade.getText()));
                        pedido_produto.add(pp);
                    }
                    jTextFieldProdutoQuantidade.setText("1");
                    carregarTabelaProdutosEscolhidos();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Informe a quantidade!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAdicionarProdutoActionPerformed

    private void jButtonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFinalizarActionPerformed
        // TODO add your handling code here:
        Fachada f = new Fachada();
        Pedido p = new Pedido();
        try {
            if (jComboBoxVendedor.getSelectedIndex() < 0) {
                throw new Exception("Informe o vendedor.");
            }
            if (jComboBoxCliente.getSelectedIndex() < 0) {
                throw new Exception("Informe o cliente.");
            }

            p.setData(jTextFieldData.getText());
            p.setVendedor(vendedores.get(jComboBoxVendedor.getSelectedIndex()));
            p.setCliente(clientes.get(jComboBoxCliente.getSelectedIndex()));
            p.setProdutos(pedido_produto);
            f.cadastrar(p);

            JOptionPane.showMessageDialog(null, "Pedido registrado com sucesso.");
            jTextFieldData.setText("");
            pedido_produto = new ArrayList<>();
            jComboBoxVendedor.setSelectedIndex(-1);
            jComboBoxCliente.setSelectedIndex(-1);
            jComboBoxProduto.setSelectedIndex(-1);
            jLabelVendedorInfo.setText("CPF: ");
            jLabelClienteInfo.setText("CPF: ");
            jLabelProdutoInfo.setText("Preço: ");
            carregarTabelaProdutosEscolhidos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButtonFinalizarActionPerformed

