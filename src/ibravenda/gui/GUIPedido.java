    private void popularComboProduto() {
        try {
            Produto filtro = new Produto();
            Fachada f = new Fachada();
            produtos = f.listar(filtro);

            DefaultComboBoxModel modelo = new DefaultComboBoxModel();

            if (produtos.size() > 0) {
                for (int i = 0; i < produtos.size(); i++) {
                    Produto p = produtos.get(i);
                    modelo.addElement(p.getDescricao());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum produto cadastrado.", "Atenção", JOptionPane.WARNING_MESSAGE);
                jButtonFinalizar.setEnabled(false);
            }

            jComboBoxProduto.setModel(modelo);
            jComboBoxProduto.setSelectedIndex(-1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }

