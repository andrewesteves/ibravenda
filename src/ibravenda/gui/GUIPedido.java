    private void carregarTabelaProdutosEscolhidos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"Descrição", "Preço", "Quantidade", "Total"});
        for (PedidoProduto pp : pedido_produto) {
            modelo.addRow(new Object[]{
                pp.getProduto().getDescricao(),
                pp.getProduto().getPreco(),
                pp.getQuantidade(),
                String.format("%.2f", pp.getTotal())
            });
        }
        jTableProdutosPedido.setModel(modelo);
        Pedido p = new Pedido();
        p.setProdutos(pedido_produto);
        jLabelTotalInfo.setText("Total: R$ " + String.format("%.2f", p.getTotalPedido()));
    }

