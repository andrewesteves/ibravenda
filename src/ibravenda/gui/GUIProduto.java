    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Produto p = new Produto();
            p.setDescricao(jTextFieldDescricao.getText());
            if (jTextFieldPreco.getText().trim().equals("") == false) {
                p.setPreco(Float.parseFloat(jTextFieldPreco.getText()));
            }
            if (jTextFieldEstoque.getText().trim().equals("") == false) {
                p.setEstoque(Integer.parseInt(jTextFieldEstoque.getText()));
            }
            Fachada f = new Fachada();
            f.cadastrar(p);
            limparForm();
            listar();
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jTableProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutoMouseClicked
        // TODO add your handling code here:
        Produto p = produtos.get(jTableProduto.getSelectedRow());
        jLabelCodigoInfo.setText(Integer.toString(p.getCodigo()));
        jTextFieldDescricao.setText(p.getDescricao());
        jTextFieldPreco.setText(Float.toString(p.getPreco()));
        jTextFieldEstoque.setText(Integer.toString(p.getEstoque()));
    }//GEN-LAST:event_jTableProdutoMouseClicked

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:
        try {
            Produto p = new Produto();
            if (jLabelCodigoInfo.getText().trim().equals("") == false) {
                p.setCodigo(Integer.parseInt(jLabelCodigoInfo.getText()));
            }
            p.setDescricao(jTextFieldDescricao.getText());
            if (jTextFieldPreco.getText().trim().equals("") == false) {
                p.setPreco(Float.parseFloat(jTextFieldPreco.getText()));
            }
            if (jTextFieldEstoque.getText().trim().equals("") == false) {
                p.setEstoque(Integer.parseInt(jTextFieldEstoque.getText()));
            }
            Fachada f = new Fachada();
            f.atualizar(p);
            limparForm();
            listar();
            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

