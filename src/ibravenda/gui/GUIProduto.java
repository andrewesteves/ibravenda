    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        // TODO add your handling code here:
        try {
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente remover?", "Atenção", JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE) == JOptionPane.OK_OPTION) {
                Produto p = new Produto();
                if (jLabelCodigoInfo.getText().trim().equals("") == false) {
                    p.setCodigo(Integer.parseInt(jLabelCodigoInfo.getText()));
                    Fachada f = new Fachada();
                    f.remover(p);
                    JOptionPane.showMessageDialog(null, "Produto removido com sucesso!");
                    limparForm();
                    listar();
                }else {
                    JOptionPane.showMessageDialog(null, "Selecione o produto.", "Atenção", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

    private void jButtonFiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFiltroActionPerformed
        // TODO add your handling code here:
        try {
            Produto p = new Produto();
            if (jLabelCodigoInfo.getText().trim().equals("") == false) {
                p.setCodigo(Integer.parseInt(jLabelCodigoInfo.getText()));
            }
            if (jTextFieldDescricao.getText().trim().equals("") == false) {
                p.setDescricao("%" + jTextFieldDescricao.getText() + "%");
            }
            if (jTextFieldPreco.getText().trim().equals("") == false) {
                p.setPreco(Float.parseFloat(jTextFieldPreco.getText()));
            }
            if (jTextFieldEstoque.getText().trim().equals("") == false) {
                p.setEstoque(Integer.parseInt(jTextFieldEstoque.getText()));
            }

            Fachada f = new Fachada();
            produtos = f.listar(p);

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"CÓDIGO", "DESCRIÇÃO", "PREÇO", "ESTOQUE"});

            for (Produto produto : produtos) {
                modelo.addRow(new Object[]{
                    produto.getCodigo(),
                    produto.getDescricao(),
                    produto.getPreco(),
                    produto.getEstoque()
                });
            }
            jTableProduto.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonFiltroActionPerformed

    /**
     * Listar clientes
     */
    private void listar() throws Exception {
        Produto p = new Produto();
        Fachada f = new Fachada();
        produtos = f.listar(p);

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"CÓDIGO", "DESCRIÇÃO", "PREÇO", "ESTOQUE"});

        for (Produto produto : produtos) {
            modelo.addRow(new Object[]{
                produto.getCodigo(),
                produto.getDescricao(),
                produto.getPreco(),
                produto.getEstoque()
            });
        }
        jTableProduto.setModel(modelo);
    }

    /**
     * Limpar os campos do formulário
     */
    private void limparForm() {
        jLabelCodigoInfo.setText("");
        jTextFieldDescricao.setText("");
        jTextFieldPreco.setText("");
        jTextFieldEstoque.setText("");
    }

